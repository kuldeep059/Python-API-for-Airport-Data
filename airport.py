@app.route('/airports')
def get_airports():
    query = request.args.get('q')

    if query:
        matching_airports = {
            iata: data
            for iata, data in airports.items()
            if query.lower() in data['name'].lower() or query.lower() in data['country'].lower()
        }
    else:
        matching_airports = airports 

    # Format the results as a list of dictionaries for dropdown
    formatted_results = [
        {
            "value": f"{data['name']} ({iata})",  # Value for the dropdown option
            "label": f"{data['name']} ({iata}) - {data['country']}"  # Label displayed to the user
        }
        for iata, data in matching_airports.items()
    ]

    return jsonify(formatted_results)

